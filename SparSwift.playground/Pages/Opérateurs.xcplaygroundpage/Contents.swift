/*:
 # LES OPÉREATEURS
 
 
 Les opérateurs sont des opérations qui peuvent être effecuté entre 2 variables.
 Il existe de base 5 opérations différentes.
 
 Signe et sifnigicaiton
 * \(+)  -> Addition ou Contaténation
 * \(-)  ->  Soustraction
 * \(*) ->  Multiplication
 * \(/)  ->  Division
 * \(%)  ->  Modulo
 
 TOutes ces opérations ne peuvent pas être effectuer sur tout les types de varaibels.
 
 En effet on ne peux pas soustraire 2 Bool ou encore diviser 2 String.
 
 On peut effectuer une additions sur tout les types nombre ( Int , Double, Float etc ... ) et sur les String
 
 La soustraction , la multilplication la division et le modulo ne peuvent être utilisé que pour des opérations arithmétiques.
 
 Seul l'addition peut être effectué sur des types non arithmetique, les String on appelera cela la concaténation.
 

  > **Important** :
 On ne peux effectuer des opérations sur des variables que si les 2 variables sont de même type
 */
/*:
 
 

 ## LA SYNTAXE

 
     // Simple

     let concatenation = "Bonjour " + "Roger" // concaténation entre 2 string contiendra alors "Bonjour Roger"

     let addition = 1 + 2 // addition contiendra alors 3
     let soustraction = 10 - 5 // soustraction contiendra alors 5
 
     // Parenthèses
     let calcul1 = (7 + 3) * 2 // 20
 
     // Variables
     let nombre1 = 7
     let nombre2 = 2
     let calcul3 = (nombre1 + nombre2) * nombre2 // 18
 

